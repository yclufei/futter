g_translate_item = function(source,target,itemdata)
	if g_translate_templete[source] then
		if g_translate_templete[source][target] then
			local templete = g_translate_templete[source][target]
			local ret = {}
			for a,b in pairs(itemdata) do
				local key = templete[a]
				if key then
					ret[key] = b
				end
			end
			return true,ret
		else
			cerror('g_translate_item false 1',source,target)			
		end
	else
		cerror('g_translate_item false 2',source,target)		
	end
	cerror('g_translate_item false 3',source,target)		
	return false,{}
end


g_translate_array = function(source,target,arraydata)
	if g_translate_templete[source] then
		if g_translate_templete[source][target] then
			local templete = g_translate_templete[source][target]
			local ret = {}
			for k,v in GSortedPairs4(arraydata) do
				ret[k] = {}
				for a,b in pairs(v) do
					local key = templete[a]
					if key then
						--print(k,a,key)
						ret[k][key] = b
					end
				end
			end
			return true,ret
		else
			cerror('g_translate_array false 1',source,target)			
		end
	else
		cerror('g_translate_array false 2',source,target)
	end
	return false,{}
end

